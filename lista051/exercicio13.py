"""
13) Desenvolver um programa que imprima a tabuada de 3 a 6.
"""

contador = 1
contador2 = 1

print("Tábuada de 3:")
while contador <= 10:
    print(f"3 * {contador} = {3 * contador}")
    contador = contador + 1

print("\nTábuada de 6:")
while contador2 <= 10:
    print(f"6 * {contador2} = {6 * contador2}")
    contador2 = contador2 + 1